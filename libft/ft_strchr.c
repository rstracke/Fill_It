#include "libft.h"

char		*ft_strchr(const char *str, int c)
{
	size_t	i;

	i = 0;
	while (str[i] && str[i] != (char)c)
		i++;
	return ((!str[i] && (char)c) ? NULL : (char *)str + i);
}
